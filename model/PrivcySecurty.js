const mongoose = require("mongoose");

const PrivacySecuritySchema = mongoose.Schema(
  {
    details: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const PrivacySecurity = mongoose.model("PrivacySecurity", PrivacySecuritySchema);

// Function to create a default policy document
async function createDefaultPolicy() {
  const existingPolicy = await PrivacySecurity.findOne();
  if (!existingPolicy) {
    await PrivacySecurity.create({
      details: `
      **سياسة الخصوصية - شركة ألوان المسافر**



مرحبًا بك في شركة **ألوان المسافر**. نحن نولي أهمية كبيرة لخصوصية عملائنا ونسعى لحماية بياناتهم الشخصية. توضح هذه السياسة كيفية جمع واستخدام وحماية المعلومات الشخصية الخاصة بك عند استخدام موقعنا الإلكتروني وخدماتنا.

---

### **1. المعلومات التي نجمعها**

عند استخدام موقعنا أو خدماتنا، قد نقوم بجمع المعلومات التالية:
- **المعلومات الشخصية**: الاسم، رقم الهاتف، البريد الإلكتروني، العنوان، بيانات جواز السفر، ومعلومات الدفع عند الحجز.
- **المعلومات التقنية**: عنوان IP، نوع المتصفح، نظام التشغيل، وسجل التصفح داخل الموقع.
- **معلومات الحجز والسفر**: بيانات الرحلات، مواعيد الحجز، تفاصيل التأشيرات، والفنادق المختارة.

---

### **2. كيفية استخدام المعلومات**

نستخدم المعلومات التي نجمعها للأغراض التالية:
- معالجة حجوزات الطيران والفنادق والتأشيرات.
- تحسين خدماتنا وتجربة المستخدم.
- إرسال إشعارات حول حالة الحجز والعروض الخاصة.
- الامتثال للمتطلبات القانونية والتنظيمية.

---

### **3. مشاركة المعلومات**

نحن لا نبيع أو نؤجر بياناتك الشخصية لأي طرف ثالث، ولكن قد نقوم بمشاركتها في الحالات التالية:
- مع مزودي الخدمات مثل شركات الطيران والفنادق وسلطات التأشيرات لضمان تنفيذ حجوزاتك.
- مع الجهات القانونية أو الحكومية إذا كان ذلك مطلوبًا بموجب القانون.
- مع مزودي خدمات الدفع لمعالجة المعاملات المالية بأمان.

---

### **4. حماية المعلومات**

نحن نتخذ جميع الإجراءات الأمنية لحماية بياناتك الشخصية، بما في ذلك:
- تشفير البيانات الحساسة.
- تقييد الوصول إلى المعلومات على أساس الحاجة فقط.
- استخدام جدران الحماية وتقنيات الأمان المتقدمة.

---

### **5. حقوق المستخدم**

يحق لك:
- الوصول إلى بياناتك الشخصية التي نحتفظ بها.
- طلب تعديل أو حذف معلوماتك.
- إلغاء الاشتراك في الرسائل الترويجية في أي وقت.

---

### **6. ملفات تعريف الارتباط (Cookies)**

نستخدم ملفات تعريف الارتباط لتحسين تجربة المستخدم وتحليل أداء الموقع. يمكنك ضبط إعدادات المتصفح لتعطيل ملفات تعريف الارتباط إذا رغبت في ذلك.

---

### **7. التعديلات على سياسة الخصوصية**

قد نقوم بتحديث سياسة الخصوصية من وقت لآخر، وسيتم نشر أي تغييرات على هذه الصفحة مع تحديث تاريخ آخر تعديل.




**شكراً لاختيارك ألوان المسافر!**
`,
    });
  }
}

module.exports = { PrivacySecurity, createDefaultPolicy };
